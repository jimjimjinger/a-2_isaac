"""manual/ 폴더 → ultralytics YOLO 학습 폴더 구조 build.

입력:
  dataset/manual/{blue,green,yellow}/*.png
  dataset/manual/labels/{blue,green,yellow}/*.txt

출력 (dataset/yolo/):
  train/images/*.png   train/labels/*.txt
  val/images/*.png     val/labels/*.txt
  data.yaml
  classes.txt

사용:
  python3 scripts/build_yolo_dataset.py                    # 기본 85/15
  python3 scripts/build_yolo_dataset.py --val-ratio 0.2
  python3 scripts/build_yolo_dataset.py --copy             # symlink 대신 복사
"""
from __future__ import annotations

import argparse
import random
import shutil
from pathlib import Path

PKG = Path(__file__).resolve().parents[1]
SRC = PKG / "dataset" / "manual"
DST = PKG / "dataset" / "yolo"

CLASSES = ["blue_mineral", "yellow_mineral", "green_gas"]
FOLDERS = ["blue", "green", "yellow", "negative"]  # negative = empty labels


def collect_pairs() -> list[tuple[Path, Path]]:
    pairs = []
    for f in FOLDERS:
        img_dir = SRC / f
        if not img_dir.exists():
            print(f"  [warn] missing folder: {f}")
            continue
        # MakeSense / manual workflow 는 라벨을 이미지와 같은 폴더에 저장
        for img in sorted(img_dir.glob("*.png")):
            lbl = img.with_suffix(".txt")
            if not lbl.exists():
                print(f"  [skip] no label for {img.name}")
                continue
            pairs.append((img, lbl))
    return pairs


def place(pair: tuple[Path, Path], split: str, copy: bool):
    img, lbl = pair
    img_dst = DST / split / "images" / img.name
    lbl_dst = DST / split / "labels" / lbl.name
    img_dst.parent.mkdir(parents=True, exist_ok=True)
    lbl_dst.parent.mkdir(parents=True, exist_ok=True)
    for src, dst in [(img, img_dst), (lbl, lbl_dst)]:
        if dst.exists() or dst.is_symlink():
            dst.unlink()
        if copy:
            shutil.copy(src, dst)
        else:
            dst.symlink_to(src.resolve())


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--val-ratio", type=float, default=0.15)
    ap.add_argument("--seed", type=int, default=42)
    ap.add_argument("--copy", action="store_true",
                    help="symlink 대신 실제 복사 (export 용)")
    args = ap.parse_args()

    # 기존 train/val 폴더 정리
    for split in ("train", "val"):
        d = DST / split
        if d.exists():
            shutil.rmtree(d)

    pairs = collect_pairs()
    if not pairs:
        raise SystemExit("no (image,label) pairs found.")

    random.seed(args.seed)
    random.shuffle(pairs)

    n = len(pairs)
    n_val = max(1, int(n * args.val_ratio))
    val_pairs = pairs[:n_val]
    train_pairs = pairs[n_val:]

    for p in train_pairs:
        place(p, "train", args.copy)
    for p in val_pairs:
        place(p, "val", args.copy)

    # data.yaml
    yaml_text = (
        f"# auto-generated by build_yolo_dataset.py\n"
        f"path: {DST}\n"
        f"train: train/images\n"
        f"val: val/images\n\n"
        f"nc: {len(CLASSES)}\n"
        f"names: {CLASSES}\n"
    )
    (DST / "data.yaml").write_text(yaml_text)

    # classes.txt (호환용)
    (DST / "classes.txt").write_text("\n".join(CLASSES) + "\n")

    # 통계
    def cls_count(pairs):
        c = {n: 0 for n in CLASSES}
        for _, lbl in pairs:
            for line in lbl.read_text().splitlines():
                if not line.strip():
                    continue
                cid = int(line.split()[0])
                c[CLASSES[cid]] += 1
        return c

    tr_c = cls_count(train_pairs)
    va_c = cls_count(val_pairs)
    print(f"  total pairs : {n}")
    print(f"  train       : {len(train_pairs)}  bbox: {tr_c}")
    print(f"  val         : {len(val_pairs)}  bbox: {va_c}")
    print(f"  output      : {DST}")
    print(f"  data.yaml   : {DST / 'data.yaml'}")
    print(f"  mode        : {'copy' if args.copy else 'symlink'}")


if __name__ == "__main__":
    main()
